#include <stdio.h>

// Variable declaration assignment and Initialization

 int main(){

     //---------------------------------------------------
     int num; //variable declaration

     num = 10 ; // variable assignment 

     printf("num = %d\n",num); // num= 10

    //---------------------------------------------------

     int x = 4; // Variable Initialization
     printf("num = %d\n",x);   

     x = 5; //1st assignment 
     printf("num after 1st assignment = %d\n",x); 

     x = 7; //2nd assignment
     printf("num after 2nd assignment = %d\n",x);  

     //Assignments can be done multiple number of times
     return 0;
 }