#include <stdio.h>

 int main(){
     
     //to print a number
     printf("%d",10);
     printf("\nnum = %d",10);

     //to print a char
     printf("\n%c",'A');
     printf ("\nChar = %c",'A');

     // To print a double
     printf("\n%lf",12.34);
     printf("\ndouble = %lf",12.34);

     return 0;
 }